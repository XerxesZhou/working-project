		/// 
		///</sumary>
		public string PageFilePath
		{
			get{return _pagefilepath;}
			set{_pagefilepath = value;}
		}
		///<sumary>
		/// 
		///</sumary>
		public int FunctionCount
		{
			get{return _functioncount;}
			set{_functioncount = value;}
		}
	}
}
