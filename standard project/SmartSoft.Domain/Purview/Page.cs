		/// 
		///</sumary>
		public string PageName
		{
			get{return _pagename;}
			set{_pagename = value;}
		}
		///<sumary>
		/// 
		///</sumary>
		public int PageSortId
		{
			get{return _pagesortid;}
			set{_pagesortid = value;}
		}
